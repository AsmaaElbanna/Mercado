package com.iti.mercado.model;

public class HomeAppliance extends Item {

    private String color;
    private String country;
    private String item_Features;
    private String item_brand;
    private String item_capacity;
    private String item_type;
    private String model_number;
    private String power;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getItem_Features() {
        return item_Features;
    }

    public void setItem_Features(String item_Features) {
        this.item_Features = item_Features;
    }

    public String getItem_brand() {
        return item_brand;
    }

    public void setItem_brand(String item_brand) {
        this.item_brand = item_brand;
    }

    public String getItem_capacity() {
        return item_capacity;
    }

    public void setItem_capacity(String item_capacity) {
        this.item_capacity = item_capacity;
    }

    public String getItem_type() {
        return item_type;
    }

    public void setItem_type(String item_type) {
        this.item_type = item_type;
    }

    public String getModel_number() {
        return model_number;
    }

    public void setModel_number(String model_number) {
        this.model_number = model_number;
    }

    public String getPower() {
        return power;
    }

    public void setPower(String power) {
        this.power = power;
    }
}
